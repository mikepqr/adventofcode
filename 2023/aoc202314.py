import itertools

import numpy

ROUNDED = "O"
CUBE = "#"
EMPTY = "."


def ascii2array(txt: str):
    return numpy.array([list(line) for line in txt.splitlines()])


def array2ascii(arr: numpy.ndarray):
    return "\n".join("".join(c for c in line) for line in arr)


def roll_north(data):
    for y, line in enumerate(data[1:], start=1):
        for x, c in enumerate(line):
            if c == ROUNDED:
                empty_column_height = sum(
                    1
                    for _ in itertools.takewhile(
                        lambda c: c == EMPTY, data[y - 1 :: -1, x]
                    )
                )
                if empty_column_height > 0:
                    newy = y - empty_column_height
                    data[newy, x] = ROUNDED
                    data[y, x] = EMPTY
    return data


def load(data):
    return sum(
        sum(c == ROUNDED for c in line) * y
        for y, line in enumerate(data[::-1], start=1)
    )


def part1():
    arr = ascii2array(data)
    print(array2ascii(arr))
    print()
    arr = roll_north(arr)
    print(array2ascii(arr))
    return load(arr)


def cycle(arr):
    for _ in range(4):
        arr = roll_north(arr)
        arr = numpy.rot90(arr, 3)
    return arr


def part2():
    n_cycles = 1000000000
    arr = ascii2array(data)
    # map of arrangement to number of cycles after which arrangement is seen
    seen = {}

    # turns out that, after a short start, we enter a loop of seeing the same
    # arrangements again, so we can truncate the calculation once we see an
    # arrangement for the second time and "fast-forward" to N cycles by a
    # lookup.
    for i in itertools.count():
        # turn the array into a hashable object
        signature = tuple(tuple(x for x in row) for row in arr)
        if signature in seen:
            break
        else:
            seen[signature] = i
            arr = cycle(arr)

    repeat_period = i - seen[signature]
    start_length = i - repeat_period
    repeat_step_at_finish = (n_cycles - start_length) % repeat_period

    # invert the seen dictionary to map from step in the repeating loop to
    # arrangements so we can look up the answer
    cycle_step = {i - start_length: arr_tuple for arr_tuple, i in seen.items()}
    return load(cycle_step[repeat_step_at_finish])


